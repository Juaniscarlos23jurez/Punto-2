package com.example.punto2.datos;

public class empleadojava {
    String nombre;
    int sueldo;
    String correo;
    String contrase;
    String horaEntrada;
    String horaSalida;
    String tiempo;
    public String getCorreo() {
        return correo;
    }
    public String getTiempo() {
        return tiempo;
    }

    public String getContrase() {
        return contrase;
    }
    public String getHoraEntrada() {
        return horaEntrada;
    }
    public String getHoraSalida() {
        return horaSalida;
    }
    public String getNombre() {
        return nombre;
    }
    public int getSueldo() {
        return sueldo;
    }
 }
